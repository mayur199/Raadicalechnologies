package example;

public class NumberSystems {
	public static void main(String[] args) {
		// Decimal number
		int decimalNumber = 100;
		System.out.println("Decimal: " + decimalNumber);

		// Binary number
		int binaryNumber = 0b1100100; // Binary representation of 100
		System.out.println("Binary: " + binaryNumber);

		// Octal number
		int octalNumber = 0144; // Octal representation of 100
		System.out.println("Octal: " + octalNumber);

		// Hexadecimal number
		int hexadecimalNumber = 0x64; // Hexadecimal representation of 100
		System.out.println("Hexadecimal: " + hexadecimalNumber);
	}
}
